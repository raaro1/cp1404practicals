try:
    numerator = int(input("Enter the numerator: "))
    denominator = int(input("Enter the denominator: "))
    while denominator <= 0:
        print("Denominator must be greater than 0.")
        denominator = int(input("Enter the denominator: "))

    fraction = numerator / denominator
    print(fraction)
except ValueError:
    print("Numerator and denominator must be valid numbers!")
except ZeroDivisionError:
    print("Cannot divide by zero!")
print("Finished.")

# 1.
# A value error will occur when a value other than an integer is entered

# 2.
# When the value being divided is less than or equal to zero

# 3.
# You could check the denominator before dividing instead of using an exception